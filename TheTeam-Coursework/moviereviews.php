                <h3> The Dark Knight Rises (2012) </h3>
                
                <p>Batman's creator Bob Kane and his fellow comic-strip artists were all admirers of Fritz Lang's German movies, the forerunners of film noir, but this did not prevent them from becoming the object of a ferocious assault by Eisenhower-era moralists bent on suppressing horror comics during a crusade led by the psychiatrist Fredric Wertham. His 1954 book, Seduction of the Innocent, attacked Bruce Wayne and Dick Grayson's menage as a covert celebration of homosexuality. Ten years later, however, when Susan Sontag's seminal essay Notes on Camp promoted kitsch and the idea of "it's good because it's bad", Batman became a TV series in garish comic-strip colour and was followed by a tongue-in-cheek film version and a revival of the low-budget 1943 Batman serial, all 15 chapters being shown back-to-back to open London's latest super cinema. <br> <br>

                By 1989 the comic strip had been elevated to the status of "graphic novel", and Tim Burton invited us to give up our search for the inner child and find our dark sides in Michael Keaton's Batman, a Hamlet-like malcontent. Then, in the wake of 9/11, Nolan followed his complex Memento and Insomnia by reviving a now-moribund franchise. To misquote Scott Fitzgerald, he embarked on the search for the soul of the dark knight where it is always three o'clock in the morning. In 2005's Batman Begins he provided a new account of the creation myth, adding some kung fu, a touch of Da Vinci Code conspiracy and a dash of Bond to Kane's 1939 account of Bruce Wayne's orphaning. It was followed in 2008 by The Dark Knight, in which Heath Ledger takes the story to exhilarating heights of terror as the demonic Joker, an implacable enemy of Batman and mankind, part Lucifer, part Loki, part Osama bin Laden.</p>
                
